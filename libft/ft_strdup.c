#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*a;
	int		size;
	int		i;

	size = ft_strlen(s1);
	a = malloc(sizeof(char) * size + 1);
	if (a == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
